/*

Operadores de comparacao

> maior que
>= maior que ou igual a

< menor que
<= menor que ou iguala

== igualdade (valor)
=== igualdade estrita (valor e tipo)

!= diferente (valor)
!=== diferente estrito (valor e tipo)
*/

console.log(10 > 5)
console.log(10 >= 5)

console.log(10 < 5)
console.log(10 <= 5)

console.log(10 == 5)
console.log(10 === 5)

console.log(10 != 5)
console.log(10 !==  5)
